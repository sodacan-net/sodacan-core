package net.sodacan.config;

import java.util.List;

public class Rules {

	private String mode;
	
	private List<String> files;

	public String getMode() {
		return mode;
	}

	public void setMode(String mode) {
		this.mode = mode;
	}

	public List<String> getFiles() {
		return files;
	}

	public void setFiles(List<String> files) {
		this.files = files;
	}

	
}
